									<h2><span class="span">WHAT IS LEADERBOARD?</span></h2>
									<p><b>Leaderboard</b> is a board showing the rank of leaders in our referral program. It is created to help all our members to grow in wealth and become more financially stable. As we are ready to be paying you monthly commission based on our leaderboard policy.</p>
									<h2><span class="span">How does the leaderboard works?</span></h2>
									<p>You will be paid a minimum of <b>₦5,000</b> and maximum of <b>₦100,000</b> every month once you refer atleast 10 subscribed members within the duration of each month.</p>
                                    <p>This does not affect the <b>10% commission</b> you will earn when they subscribe and also the commission you will earn whenever they made a transaction</p>
									<h2><span class="span">How much can I make within a month?</span></h2>
                                    <p>You can make up to <b>₦100,000</b> within a month.</p>
                                    <ul>
                                    <li> If you refer <b>10 Basic subscribers</b>, you will earn <b>₦500</b> on each subscriber which the total is <b>₦5,000</b>. </li>
                                    <li> If you refer <b>10 Value subscribers</b>, you will earn <b>₦1,000</b> on each subscriber which the total is <b>₦10000</b>. </li>
									<li> If you refer <b>10 Premium subscribers</b>, you will earn <b>₦1,500</b> on each subscriber which the total is <b>₦15,000</b>.</li>
									<li> If you refer <b>10 Diamond subscribers</b>, you will earn <b>₦2,000</b> on each subscriber which the total is <b>₦20,000</b>.</li>
									<li> If you refer <b>10 Gold subscribers</b>, you will earn <b>₦2,500</b> on each subscriber which the total is <b>₦25,000</b>.</li>
                                    <li> And also if you can refer up to <b>20 Gold subscribers</b>, you will earn <b>₦100,000</b> in total.</li>
                                    </ul>
									<h2><span class="span">How do I get paid?</span></h2>
                                    <p>Once you have referred up to 10 subscribed members, your money will be added to your commission in your dashboard which you can withdraw to your bank account or use it to purchase any product.</p>
                                    <h2><span class="span">How do I see my leaderboard earnings?</span></h2>
                                    <p>You can see it in your dashboard. Log into your account, click on <b>MENU</b>, then click on ><b>My Leaderboard</b>.</p>
                                    <h2><span class="span">Who is qualified in the leaderboard program?</span></h2>
                                    <p>Only subscribed members are qualified in the leaderboard program. <b>Free members are not qualified.</b></p>
                                    <h2><span class="span">How many subscriber must I refer within a month?</span></h2>
                                    <p>You must refer atleast <b>10 subscribed members</b> within a month..</p>
                                    <h2><span class="span">What if I'm unable to refer up to 10 subscribers within a month?</span></h2>
                                    <p>If your subscribed referrals are not up-to 10 before the month ends, all the learderboard money earned from those that you referred in that particular month will be automatically converted into Point Value(PV). And the conversion will be 10% of the amount earned. So if you earn ₦3,000 in your leaderboard within a month and you are unable to refer up-to 10 Subscribed Members, the ₦3,000 will be converted to 300PV which will be added to your Point Value. </p>
                                    <h2><span class="span">Will I be paid if my referrals are Free Members?</span></h2>
                                    <p>No. You will only be paid if your referrals are <b>Subscribed members.</b></p>
									<h2><span class="span">When will I be paid?</span></h2>
                                    <p>Our payment for leaderboard is <b>12A.M</b> on the <b>1st</b> of each month.</p>
